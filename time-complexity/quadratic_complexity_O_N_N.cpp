#include <bits/stdc++.h>
using namespace std;

int mn()
{

    int n, m;
    cin >> n >> m;
    for (int i = 0; i < n; i++)
    {
        for (int m = 0; m < n; m++)
        {
            cout << "Hello c++" << endl;
        }
    }

    return 0;
}

// complexity = n*m

int nnn()
{

    int n;
    cin >> n;
    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < n; j++)
        {
            for (int k = 0; k < n; k++)
            {
                cout << "Hello c++" << endl;
            }
        }
    }

    return 0;
}
// complexity = n*n*n

